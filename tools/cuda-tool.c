#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <assert.h>

#include "cuda-api.h"

int main(int argc, char **argv)
{
	cuda_context_t * cuda = cuda_context_init(NULL, NULL);
	assert(cuda);
	
	int devices_count = cuda->devices_count;
	if(devices_count <= 0) exit(1);
	
	cuda_device_t * gpu = &cuda->devices[0];
	printf("%d\n", gpu->arch);

	cuda_context_cleanup(cuda);
	return 0;
}

